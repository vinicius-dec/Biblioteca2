function mostrarLivrosDeMesmaCategoria(event) {
  event.preventDefault();
  const categoria = event.target.id;
  let container = document.querySelector(".container");
  container.innerHTML = "";

  livros.forEach((livro) => {
    if (livro.categorias === categoria) {
      container.innerHTML += ` 
      <div class="card" data-id="${livro.id}">
        <a href="./pagina_livros.html?id=${livro.id}">
          <div class="imagem">
            <img class="img" src="${livro.imagem}" alt="" />
          </div>
          <div class="nome">
            <p>${livro.titulo}</p>
          </div>
        </a>
      </div>`;
    }
    console.log(livro.id);
    
  })

}


